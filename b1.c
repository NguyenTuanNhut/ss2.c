#include <stdio.h>

int main() {
    // Kiểu int: dùng để lưu trữ số nguyên (vd: -10, 0, 20)
    int age = 25; // Khai báo và khởi tạo biến kiểu int

    // Kiểu float: dùng để lưu trữ số thực (vd: 3.14, -0.5)
    float height = 5.9; // Khai báo và khởi tạo biến kiểu float

    // Kiểu double: giống float nhưng có độ chính xác cao hơn
    double pi = 3.14159265359; // Khai báo và khởi tạo biến kiểu double

    // Kiểu char: dùng để lưu trữ một ký tự (vd: 'A', 'z')
    char grade = 'A'; // Khai báo và khởi tạo biến kiểu char

    // Kiểu _Bool (hoặc bool trong stdbool.h): dùng để lưu giá trị đúng/sai (true/false)
    _Bool isPassed = 1; // Khai báo và khởi tạo biến kiểu _Bool (1: true, 0: false)

    
    return 0;
}
